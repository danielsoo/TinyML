#!/usr/bin/env python3
"""
FGSM Adversarial Training (AT) + Evaluation on the final 6 TFLite models.

Pipeline per model:
  1. Pre-AT eval  — evaluate TFLite at 6 epsilons (FGSM generated by surrogate)
  2. AT           — fine-tune a Keras surrogate (global_model.h5) with FGSM examples
  3. Post-AT eval — re-evaluate TFLite at 6 epsilons (FGSM from AT-hardened surrogate)

Why surrogate? TFLite models are not trainable. AT is applied to the surrogate
(global_model.h5) and we measure whether the compressed TFLite models transfer
the robustness — i.e. how well each compression strategy holds up against a
smarter attacker.

Inputs:
  data/processed/pgd_sweep/fgsm_final_six.json   (written by run_pgd_on_sweep.py)
  models/global_model.h5                          (surrogate for attack generation)

Outputs:
  data/processed/fgsm_at/fgsm_eval_results.csv
  data/processed/fgsm_at/fgsm_eval_results.md
  data/processed/fgsm_at/plots/                  (epsilon sweep curves, before/after bar)

Usage:
  python scripts/run_fgsm_at_eval.py --config config/federated_local_sky.yaml
"""
import argparse
import csv
import json
import sys
from pathlib import Path

project_root = Path(__file__).resolve().parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

import numpy as np
import yaml
import tensorflow as tf

from src.data.loader import load_dataset
from src.adversarial.fgsm_hook import generate_fgsm_attack, generate_adversarial_dataset

SIX_JSON    = project_root / "data" / "processed" / "pgd_sweep" / "fgsm_final_six.json"
ATTACKER_H5 = project_root / "models" / "global_model.h5"
FGSM_CFG    = project_root / "config" / "fgsm.yaml"
OUT_DIR     = project_root / "data" / "processed" / "fgsm_at"

EPSILONS = [0.01, 0.05, 0.1, 0.15, 0.2, 0.3]   # 6 epsilons as in fgsm.yaml


# ── helpers ───────────────────────────────────────────────────────────────────

def _f(v, default=np.nan):
    try:
        return float(v)
    except (TypeError, ValueError):
        return default


def load_surrogate(path: Path):
    try:
        import tensorflow_model_optimization as tfmot
        with tfmot.quantization.keras.quantize_scope():
            m = tf.keras.models.load_model(str(path), compile=False)
    except Exception:
        m = tf.keras.models.load_model(str(path), compile=False)
    m.compile(optimizer=tf.keras.optimizers.Adam(1e-4),
              loss="binary_crossentropy", metrics=["accuracy"])
    return m


def predict_tflite(interp, in_d, out_d, x: np.ndarray) -> np.ndarray:
    preds = []
    for i in range(len(x)):
        interp.set_tensor(in_d["index"], x[i:i+1].astype(np.float32))
        interp.invoke()
        out = interp.get_tensor(out_d["index"])
        preds.append(float(out.ravel()[-1]))
    return np.array(preds)


def eval_model(pred_fn, x_orig, x_adv, y_true, threshold=0.5):
    p_orig = pred_fn(x_orig)
    p_adv  = pred_fn(x_adv)
    y = np.asarray(y_true).ravel().astype(int)
    orig_acc = float(((p_orig >= threshold).astype(int) == y).mean())
    adv_acc  = float(((p_adv  >= threshold).astype(int) == y).mean())

    tp = int(((p_adv >= threshold).astype(int) == 1) & (y == 1)).sum() if hasattr(((p_adv >= threshold).astype(int) == 1) & (y == 1), 'sum') else int(np.sum(((p_adv >= threshold).astype(int) == 1) & (y == 1)))
    fp = int(np.sum(((p_adv >= threshold).astype(int) == 1) & (y == 0)))
    fn = int(np.sum(((p_adv >= threshold).astype(int) == 0) & (y == 1)))
    prec = tp / max(tp + fp, 1)
    rec  = tp / max(tp + fn, 1)
    f1   = 2 * prec * rec / max(prec + rec, 1e-9)

    return {
        "orig_acc":     orig_acc,
        "adv_acc":      adv_acc,
        "success_rate": orig_acc - adv_acc,
        "adv_f1":       f1,
        "adv_prec":     prec,
        "adv_rec":      rec,
    }


def adversarial_finetune(surrogate, x_train, y_train,
                         epsilon=0.1, epochs=3, batch_size=128):
    """Fine-tune surrogate with FGSM augmented data (standard AT)."""
    print(f"  AT fine-tuning: eps={epsilon}, epochs={epochs}, n={len(x_train)}")
    for ep in range(epochs):
        idx = np.random.permutation(len(x_train))
        losses = []
        for start in range(0, len(x_train), batch_size):
            bi = idx[start:start + batch_size]
            xb = x_train[bi].astype(np.float32)
            yb = y_train[bi].astype(np.float32)
            # Generate FGSM for this batch
            x_adv_b, _ = generate_fgsm_attack(surrogate, xb, yb, eps=epsilon)
            # Mix clean + adversarial (50/50)
            x_mix = np.concatenate([xb, x_adv_b], axis=0)
            y_mix = np.concatenate([yb, yb], axis=0)
            surrogate.train_on_batch(x_mix, y_mix)
        # Quick accuracy check
        p = surrogate.predict(x_train[:500], verbose=0).ravel()
        acc = ((p >= 0.5).astype(int) == y_train[:500].astype(int)).mean()
        print(f"    epoch {ep+1}/{epochs}  train_acc={acc:.3f}")
    return surrogate


# ── main ─────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--config",    default="config/federated_local_sky.yaml")
    parser.add_argument("--six-json",  default=str(SIX_JSON),
                        help="Path to fgsm_final_six.json from run_pgd_on_sweep.py")
    parser.add_argument("--epsilons",  nargs="+", type=float, default=EPSILONS)
    parser.add_argument("--at-epsilon",type=float, default=0.1,
                        help="Epsilon used for AT fine-tuning")
    parser.add_argument("--at-epochs", type=int, default=3)
    parser.add_argument("--n-eval",    type=int, default=5000)
    parser.add_argument("--n-train",   type=int, default=20000,
                        help="Training samples used for AT fine-tuning")
    parser.add_argument("--threshold", type=float, default=0.5)
    args = parser.parse_args()

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    plot_dir = OUT_DIR / "plots"
    plot_dir.mkdir(exist_ok=True)

    # Load config + data
    with open(args.config, encoding="utf-8") as f:
        cfg = yaml.safe_load(f)
    data_cfg = cfg.get("data", {})
    kwargs = {k: v for k, v in data_cfg.items() if k not in {"name", "num_clients"}}
    if "path" in kwargs and "data_path" not in kwargs:
        kwargs["data_path"] = kwargs.pop("path")

    print("Loading dataset ...")
    x_train_raw, y_train_raw, x_test, y_test = load_dataset(
        data_cfg.get("name", "cicids2017"), **kwargs)
    x_train = np.asarray(x_train_raw, dtype=np.float32)[:args.n_train]
    y_train = np.asarray(y_train_raw, dtype=np.float32)[:args.n_train]
    x_test  = np.asarray(x_test,  dtype=np.float32)[:args.n_eval]
    y_test  = np.asarray(y_test,  dtype=np.float32)[:args.n_eval]
    print(f"  Train: {len(x_train)}  Test: {len(x_test)}")

    # Load final 6 model list
    six_path = Path(args.six_json)
    if not six_path.exists():
        print(f"fgsm_final_six.json not found: {six_path}", file=sys.stderr)
        print("Run run_pgd_on_sweep.py first.", file=sys.stderr)
        return 1
    with open(six_path, encoding="utf-8") as f:
        six_models = json.load(f)
    print(f"\nLoaded {len(six_models)} models from {six_path.name}")

    # Load surrogate (clean)
    print(f"\nLoading surrogate: {ATTACKER_H5.name}")
    surrogate_clean = load_surrogate(ATTACKER_H5)

    # AT fine-tune surrogate — do this ONCE, shared for all models
    print("\n" + "="*60)
    print("Adversarial Training (AT) on surrogate ...")
    print("="*60)
    surrogate_at = load_surrogate(ATTACKER_H5)   # fresh copy
    surrogate_at = adversarial_finetune(
        surrogate_at, x_train, y_train,
        epsilon=args.at_epsilon, epochs=args.at_epochs,
    )

    # Evaluate each model
    all_results = []   # one row per (model × phase × epsilon)
    summary_rows = []  # one row per (model × phase) averaged

    for entry in six_models:
        tag       = entry["tag"]
        role      = entry["role"]
        tflite_p  = Path(entry["tflite"])
        if not tflite_p.is_absolute():
            tflite_p = project_root / tflite_p
        ptq       = entry["ptq"]

        print(f"\n{'─'*60}")
        print(f"  Model: {tag}")
        print(f"  Role:  {role}")
        print(f"  PTQ:   {ptq}")

        if not tflite_p.exists():
            print(f"  MISSING: {tflite_p}")
            continue

        interp = tf.lite.Interpreter(model_path=str(tflite_p))
        interp.allocate_tensors()
        in_d  = interp.get_input_details()[0]
        out_d = interp.get_output_details()[0]
        pred_fn = lambda x, i=interp, id_=in_d, od_=out_d: predict_tflite(i, id_, od_, x)

        for phase, surrogate in [("pre_AT", surrogate_clean), ("post_AT", surrogate_at)]:
            print(f"\n  [{phase}] Epsilon sweep ...")
            phase_adv_accs = []
            phase_f1s      = []

            for eps in args.epsilons:
                # Generate FGSM adversarial examples using current surrogate
                x_adv, _ = generate_fgsm_attack(
                    surrogate, x_test, y_test, eps=eps)

                m = eval_model(pred_fn, x_test, x_adv, y_test, args.threshold)

                print(f"    eps={eps:.2f}  orig={m['orig_acc']:.3f}  "
                      f"adv_acc={m['adv_acc']:.3f}  "
                      f"adv_f1={m['adv_f1']:.3f}  "
                      f"success={m['success_rate']:+.3f}")

                all_results.append({
                    "tag":          tag,
                    "role":         role,
                    "ptq":          ptq,
                    "phase":        phase,
                    "epsilon":      eps,
                    "orig_acc":     round(m["orig_acc"],     4),
                    "adv_acc":      round(m["adv_acc"],      4),
                    "adv_f1":       round(m["adv_f1"],       4),
                    "adv_prec":     round(m["adv_prec"],     4),
                    "adv_rec":      round(m["adv_rec"],      4),
                    "success_rate": round(m["success_rate"], 4),
                })
                phase_adv_accs.append(m["adv_acc"])
                phase_f1s.append(m["adv_f1"])

            summary_rows.append({
                "tag":          tag,
                "role":         role,
                "ptq":          ptq,
                "phase":        phase,
                "mean_adv_acc": round(np.mean(phase_adv_accs), 4),
                "mean_adv_f1":  round(np.mean(phase_f1s),      4),
                "min_adv_acc":  round(np.min(phase_adv_accs),  4),
                "max_adv_acc":  round(np.max(phase_adv_accs),  4),
            })

    # Write CSVs
    result_csv = OUT_DIR / "fgsm_eval_results.csv"
    fields = ["tag", "role", "ptq", "phase", "epsilon",
              "orig_acc", "adv_acc", "adv_f1", "adv_prec", "adv_rec", "success_rate"]
    with open(result_csv, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fields)
        w.writeheader()
        w.writerows(all_results)
    print(f"\nWrote {result_csv}")

    summary_csv = OUT_DIR / "fgsm_eval_summary.csv"
    sfields = ["tag", "role", "ptq", "phase",
               "mean_adv_acc", "mean_adv_f1", "min_adv_acc", "max_adv_acc"]
    with open(summary_csv, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=sfields)
        w.writeheader()
        w.writerows(summary_rows)
    print(f"Wrote {summary_csv}")

    # Write markdown report
    _write_markdown(all_results, summary_rows, args, OUT_DIR)

    # Plots
    _plot_epsilon_curves(all_results, six_models, args.epsilons, plot_dir)
    _plot_before_after_bars(summary_rows, six_models, plot_dir)
    _plot_heatmap(all_results, six_models, args.epsilons, plot_dir)

    print(f"\nAll outputs → {OUT_DIR.resolve()}")
    return 0


# ── report writers ────────────────────────────────────────────────────────────

def _write_markdown(all_results, summary_rows, args, out_dir):
    lines = [
        "# FGSM AT Evaluation Report",
        "",
        f"- AT epsilon: {args.at_epsilon}  |  AT epochs: {args.at_epochs}",
        f"- Eval epsilons: {args.epsilons}",
        f"- Eval samples: {args.n_eval}",
        "",
        "## Summary (mean across epsilons)",
        "",
        "| Tag | PTQ | Phase | Mean Adv Acc | Mean Adv F1 | Min Adv Acc |",
        "|-----|-----|-------|-------------|------------|------------|",
    ]
    for r in summary_rows:
        lines.append(
            f"| `{r['tag']}` | {'Yes' if r['ptq'] else 'No'} | {r['phase']} "
            f"| {r['mean_adv_acc']:.3f} | {r['mean_adv_f1']:.3f} | {r['min_adv_acc']:.3f} |"
        )

    lines += ["", "## Full Epsilon Sweep", "",
              "| Tag | PTQ | Phase | ε | Orig Acc | Adv Acc | Adv F1 | Success Rate |",
              "|-----|-----|-------|---|----------|---------|--------|-------------|"]
    for r in all_results:
        lines.append(
            f"| `{r['tag']}` | {'Yes' if r['ptq'] else 'No'} | {r['phase']} "
            f"| {r['epsilon']} | {r['orig_acc']:.3f} | {r['adv_acc']:.3f} "
            f"| {r['adv_f1']:.3f} | {r['success_rate']:+.3f} |"
        )

    p = out_dir / "fgsm_eval_results.md"
    p.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {p}")


def _get_tag_label(tag, max_len=30):
    """Short label for plots."""
    parts = tag.split("__")
    short = "__".join(p.replace("distill_", "d=").replace("prune_", "p=")
                        .replace("ptq_yes", "PTQ").replace("ptq_no", "noPTQ")
                        .replace("yes_qat", "QAT").replace("no_qat", "noQAT")
                      for p in parts)
    return short if len(short) <= max_len else short[:max_len] + "…"


def _plot_epsilon_curves(all_results, six_models, epsilons, plot_dir):
    """One subplot per model: adv_acc vs epsilon, pre-AT vs post-AT."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    tags = [e["tag"] for e in six_models]
    n = len(tags)
    fig, axes = plt.subplots(2, 3, figsize=(16, 9), sharey=False)
    axes = axes.flat

    colors = {"pre_AT": "#4c72b0", "post_AT": "#e05c2a"}

    for ax, tag in zip(axes, tags):
        for phase, color in colors.items():
            rows = [r for r in all_results if r["tag"] == tag and r["phase"] == phase]
            rows.sort(key=lambda r: r["epsilon"])
            eps_vals  = [r["epsilon"]  for r in rows]
            adv_accs  = [r["adv_acc"]  for r in rows]
            adv_f1s   = [r["adv_f1"]   for r in rows]
            ax.plot(eps_vals, adv_accs, marker="o", color=color,
                    label=f"{phase} acc", lw=2)
            ax.plot(eps_vals, adv_f1s,  marker="s", color=color,
                    label=f"{phase} F1",  lw=1.5, linestyle="--", alpha=0.7)

        entry = next((e for e in six_models if e["tag"] == tag), {})
        ptq_str = "PTQ" if entry.get("ptq") else "noPTQ"
        ax.set_title(f"{_get_tag_label(tag)}\n[{ptq_str}]", fontsize=7, fontweight="bold")
        ax.set_xlabel("Epsilon", fontsize=8)
        ax.set_ylabel("Accuracy / F1", fontsize=8)
        ax.set_ylim(0, 1.05)
        ax.set_xticks(epsilons)
        ax.tick_params(labelsize=7)
        ax.grid(alpha=0.3)
        ax.legend(fontsize=6, ncol=2)

    fig.suptitle("FGSM Epsilon Sweep — Pre-AT vs Post-AT (solid=acc, dashed=F1)",
                 fontsize=11, fontweight="bold")
    plt.tight_layout()
    p = plot_dir / "epsilon_curves.png"
    plt.savefig(p, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  saved {p.name}")


def _plot_before_after_bars(summary_rows, six_models, plot_dir):
    """Grouped bars: mean adv_acc before/after AT per model."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np

    tags = [e["tag"] for e in six_models]
    labels = [_get_tag_label(t, 25) for t in tags]
    x = np.arange(len(tags))
    bar_w = 0.35

    pre_accs  = []
    post_accs = []
    pre_f1s   = []
    post_f1s  = []

    for tag in tags:
        pre  = next((r for r in summary_rows if r["tag"] == tag and r["phase"] == "pre_AT"),  {})
        post = next((r for r in summary_rows if r["tag"] == tag and r["phase"] == "post_AT"), {})
        pre_accs.append( _f(pre.get("mean_adv_acc")))
        post_accs.append(_f(post.get("mean_adv_acc")))
        pre_f1s.append(  _f(pre.get("mean_adv_f1")))
        post_f1s.append( _f(post.get("mean_adv_f1")))

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 6))

    for ax, pre_vals, post_vals, metric in [
        (ax1, pre_accs,  post_accs,  "Mean Adv Accuracy"),
        (ax2, pre_f1s,   post_f1s,   "Mean Adv F1"),
    ]:
        b1 = ax.bar(x - bar_w/2, pre_vals,  bar_w, color="#4c72b0", alpha=0.85, label="Pre-AT")
        b2 = ax.bar(x + bar_w/2, post_vals, bar_w, color="#e05c2a", alpha=0.85, label="Post-AT")

        for bar, v in zip(b1, pre_vals):
            if not np.isnan(v):
                ax.text(bar.get_x() + bar.get_width()/2, v + 0.01,
                        f"{v:.3f}", ha="center", fontsize=7, rotation=90)
        for bar, v in zip(b2, post_vals):
            if not np.isnan(v):
                ax.text(bar.get_x() + bar.get_width()/2, v + 0.01,
                        f"{v:.3f}", ha="center", fontsize=7, rotation=90)

        # Delta arrows
        for xi, (pv, qv) in enumerate(zip(pre_vals, post_vals)):
            if not (np.isnan(pv) or np.isnan(qv)):
                delta = qv - pv
                color = "green" if delta > 0 else "red"
                ax.annotate(f"{delta:+.2f}", xy=(xi, max(pv, qv) + 0.04),
                            ha="center", fontsize=7, color=color, fontweight="bold")

        ax.set_xticks(x)
        ax.set_xticklabels(labels, rotation=35, ha="right", fontsize=7)
        ax.set_ylabel(metric, fontsize=9)
        ax.set_ylim(0, 1.15)
        ax.set_title(f"{metric} — Pre vs Post AT", fontsize=10, fontweight="bold")
        ax.grid(axis="y", alpha=0.3)
        ax.legend(fontsize=8)

        # PTQ/noPTQ background shading
        for xi, entry in enumerate(six_models):
            if entry.get("ptq"):
                ax.axvspan(xi - 0.5, xi + 0.5, alpha=0.05, color="orange")

    fig.suptitle("Before vs After Adversarial Training  (orange shading = PTQ model)",
                 fontsize=11, fontweight="bold")
    plt.tight_layout()
    p = plot_dir / "before_after_bars.png"
    plt.savefig(p, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  saved {p.name}")


def _plot_heatmap(all_results, six_models, epsilons, plot_dir):
    """Heatmap: rows=models, cols=epsilons, cells=adv_acc. Two panels: pre/post AT."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np

    tags   = [e["tag"] for e in six_models]
    labels = [_get_tag_label(t, 28) for t in tags]
    phases = ["pre_AT", "post_AT"]

    fig, axes = plt.subplots(1, 2, figsize=(16, 5))

    for ax, phase in zip(axes, phases):
        data = np.full((len(tags), len(epsilons)), np.nan)
        for ti, tag in enumerate(tags):
            for ei, eps in enumerate(epsilons):
                row = next((r for r in all_results
                            if r["tag"] == tag and r["phase"] == phase
                            and abs(r["epsilon"] - eps) < 1e-6), None)
                if row:
                    data[ti, ei] = _f(row["adv_acc"])

        im = ax.imshow(data, cmap="RdYlGn", vmin=0, vmax=1, aspect="auto")
        ax.set_xticks(range(len(epsilons)))
        ax.set_xticklabels([f"ε={e}" for e in epsilons], fontsize=8)
        ax.set_yticks(range(len(tags)))
        ax.set_yticklabels(labels, fontsize=7)
        ax.set_title(f"Adv Accuracy — {phase}", fontsize=10, fontweight="bold")
        plt.colorbar(im, ax=ax, label="Adv Accuracy", shrink=0.8)

        for ti in range(len(tags)):
            for ei in range(len(epsilons)):
                v = data[ti, ei]
                if not np.isnan(v):
                    ax.text(ei, ti, f"{v:.2f}", ha="center", va="center",
                            fontsize=8,
                            color="white" if v < 0.4 or v > 0.8 else "black")

        # Mark PTQ rows with a border
        for ti, entry in enumerate(six_models):
            if entry.get("ptq"):
                ax.add_patch(plt.Rectangle(
                    (-0.5, ti - 0.5), len(epsilons), 1,
                    fill=False, edgecolor="orange", lw=2))

    fig.suptitle("FGSM Adversarial Accuracy Heatmap  (orange border = PTQ model)\n"
                 "Green = robust, Red = vulnerable",
                 fontsize=11, fontweight="bold")
    plt.tight_layout()
    p = plot_dir / "adv_acc_heatmap.png"
    plt.savefig(p, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  saved {p.name}")


if __name__ == "__main__":
    sys.exit(main())
